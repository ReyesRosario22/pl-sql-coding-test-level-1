CREATE TABLE `BOOK_MANAGEMENT` (
  `BOOK_ID` int(12) NOT NULL,
  `ISBN` int(12) NOT NULL,
  `BOOK_NAME` varchar(150) NOT NULL,
  `GEOLOCATION_ID` int(12) NOT NULL,
  `COUNTRY_ORIGIN` int(12) NOT NULL,
  `AUTHOR_ID` int(12) NOT NULL,
  `PUBLISHER_ID` int(12) NOT NULL,
  `SUMMARY` text DEFAULT NULL,
  `DATE_CREATED` date NOT NULL,
  `CREATED_BYID` int(12) NOT NULL,
  `DATE_UPDATED` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `UPDATED_BYID` int(12) NOT NULL,
  `DATE_DELETED` date DEFAULT NULL,
  `DELETED_BYID` int(12) DEFAULT NULL,
  `DELETED` tinyint(1) DEFAULT NULL,
  `LATEST` tinyint(1) DEFAULT NULL,
PRIMARY KEY (`BOOK_ID`),
  UNIQUE KEY `BOOKMANAGEMENT_UNIQUE` (`BOOK_ID`,`DATE_UPDATED`),
  FOREIGN KEY (`GEOLOCATION_ID`) REFERENCES GEOLOCATION(`GEOLOCATION_ID`),
  FOREIGN KEY (`AUTHOR_ID`) REFERENCES AUTHOR(`AUTHOR_ID`),
  FOREIGN KEY (`PUBLISHER_ID`) REFERENCES PUBLISHER(`PUBLISHER_ID`),
  FOREIGN KEY (`CREATED_BYID`) REFERENCES USER_MANAGEMENT(`USER_ID`),
 );
